#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int j;

	j = -1;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			j = i;
		i++;
	}
	if (s[i] == c)
		j = i;
	if (j >= 0)
		return ((char*)&s[j]);
	return (0);
}
