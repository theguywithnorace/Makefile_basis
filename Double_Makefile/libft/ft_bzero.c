#include <libc.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*tmp;

	tmp = (char*)s;
	i = -1;
	while (++i < n)
		tmp[i] = 0;
	s = (void*)tmp;
}
